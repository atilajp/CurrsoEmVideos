n = input('Digite um algo para analise! ')
print('Oque você digitou esta em caixa Alta ',n.isupper())
print('o que você digitou esta em caixa baixa ',n.islower())
print('Oque você digitou pode ser impresso ',n.isprintable())
print('Oque você digitou tem as primeiras letras de capa palavra maiúsculas ',n.istitle())
print('Oque você digitou é um espaço ',n.isspace())
print('Oque você digitou é um numero ',n.isnumeric())
print(n.isidentifier())
print(n.isdigit())
print(n.isdecimal())
print(n.isalpha())
print(n.isalnum())




